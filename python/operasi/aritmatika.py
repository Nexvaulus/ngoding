## operasi aritmatika

a = 10
b = 3

# operasi tambah +

hasil = a + b
print(a, "+", b, "=", hasil)

# operasi pengurangan -

hasil = a - b
print(a, "-", b, "=", hasil)

# operasi perkalian *

hasil = a * b
print(a, "*", b, "=", hasil)

# operasi pembagian /

hasil = a / b
print(a, "/", b, "=", hasil)

# operasi eksponen (pangkat) **

hasil = a ** b
print(a, "**", b, "=", hasil)

# operasi modulus (sisa pembagian) %

hasil = a % b
print(a, "%", b, "=", hasil)

# operasi floor division (sisa pembagian tapi dibulatkan kebawah) //

hasil = a // b
print(a, "//", b, "=", hasil)

# prioritas operasi, operational precedence
# sama kaya matematika biasa paling tinggi kurung
'''
    1. ()
    2. eksponen **
    3. perkalian dan kawan kawan nya * / ** % //
    4. pertambahan dan pengurangan + -
'''