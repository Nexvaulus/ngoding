# ELIF = else if statement

nama = input("Nama kamu siapa? ")

if nama == "ucup": # kondisi 1
    print("Hai ucup") # aksi true 1

elif nama == "otong": # kondisi 2
    print("Hai otong") # aksi true 2

elif nama == "mamat": # kondisi 3
    print("Hai mamat") # aksi true 3
    
else:
    print("au ah gak kenal") # aksi false


print("Terima kasih")